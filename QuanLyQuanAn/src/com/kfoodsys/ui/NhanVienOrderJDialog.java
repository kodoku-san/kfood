    public void loadProduct() {
        new Thread() {
            public void run() {
                try {
                    sleep(100);
                    xuLiPhanTrang();
                    if (listProduct.size() > 0) {
                        for (int i = 0; i < 9; i++) {
                            listLabelProduct.get(i).setBorder(new javax.swing.border.LineBorder(new java.awt.Color(219, 64, 68), 2, true));
                            listLabelPrice.get(8 - i).setForeground(new java.awt.Color(0, 0, 0));
                        }
                        for (int i = 0; i < itemInPage - itemThua; i++) {
                            int indexProduct = (indexPage - 1) * (int) itemInPage + i;
                            listLabelProduct.get(i).setText(listProduct.get(indexProduct).getTenSanPham());
                            listLabelProduct.get(i).setIcon(ResizeImage("imgs//" + listProduct.get(indexProduct).getHinh()));
                            String gia = currencyVN.format(listProduct.get(indexProduct).getGiaBan());
                            listLabelPrice.get(i).setText(gia);
                        }
                        for (int i = 0; i < itemThua; i++) {
                            listLabelProduct.get((int) itemInPage - 1 - i).setText("");
                            listLabelProduct.get((int) itemInPage - 1 - i).setIcon(null);
                            listLabelProduct.get((int) itemInPage - 1 - i).setBorder(null);
                            listLabelPrice.get((int) itemInPage - 1 - i).setForeground(new java.awt.Color(255, 255, 255));
                        }
                    } else {
                        for (int i = 0; i < 9; i++) {
                            listLabelProduct.get(i).setText("");
                            listLabelProduct.get(i).setIcon(null);
                            listLabelProduct.get(i).setBorder(null);
                            listLabelPrice.get(i).setForeground(new java.awt.Color(255, 255, 255));
                        }
                    }
                } catch (Exception e) {
                }
            }
        }.start();
    }

    public void xuLiPhanTrang() {
        double totalProduct = listProduct.size();
        totalPage = (int) Math.ceil(totalProduct / itemInPage);
        if (indexPage == totalPage) {
            if (totalProduct % itemInPage > 0) {
                itemThua = itemInPage - totalProduct % itemInPage;
            }
        } else {
            itemThua = 0;
        }
        lblPage.setText("Page " + indexPage + " of " + totalPage);
    }

    public void nextPage() {
        if (indexPage == totalPage) {
            indexPage = 1;
        } else {
            indexPage++;
        }
        loadProduct();
    }

    public void prevPage() {
        if (indexPage == 1) {
            indexPage = totalPage;
        } else {
            indexPage--;
        }
        loadProduct();
    }

