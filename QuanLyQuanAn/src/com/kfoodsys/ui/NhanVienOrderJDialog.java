    public ArrayList<DanhSachMon> getDanhSachMon() {
        ArrayList<DanhSachMon> list = new ArrayList<>();
        for (int i = 0; i < tblDanhSachMon.getRowCount(); i++) {
            DanhSachMon danhSachMon = new DanhSachMon();
            int soLuong = Integer.parseInt(tblDanhSachMon.getValueAt(i, 2).toString());
            boolean t = tblDanhSachMon.getValueAt(i, 4).toString().contains("Đang order");
            if(t && soLuong > 0) {
                danhSachMon.setSoLuong(Integer.parseInt(tblDanhSachMon.getValueAt(i, 2).toString()));
            } else {
                danhSachMon.setSoLuong(listDanhSachMon.get(i).getSoLuong());
            }
            danhSachMon.setIdMon(Integer.parseInt(tblDanhSachMon.getValueAt(i, 0).toString()));
            danhSachMon.setTenMon(tblDanhSachMon.getValueAt(i, 1).toString());
            String giaString = tblDanhSachMon.getValueAt(i, 3).toString().replace(" ₫", "").replace(".", "");
            danhSachMon.setGia(Double.parseDouble(giaString));
            danhSachMon.setTrangThai(tblDanhSachMon.getValueAt(i, 4).toString());
            danhSachMon.setGhiChu(tblDanhSachMon.getValueAt(i, 5).toString());
            list.add(danhSachMon);
        }
        return list;
    }

    public void setDanhSachMon() {
        DefaultTableModel model = (DefaultTableModel) tblDanhSachMon.getModel();
        model.setRowCount(0);
        for (DanhSachMon danhSachMon : listDanhSachMon) {
            model.addRow(new Object[]{
                danhSachMon.getIdMon(),
                danhSachMon.getTenMon(),
                danhSachMon.getSoLuong(),
                currencyVN.format(danhSachMon.getGia()),
                danhSachMon.getTrangThai(),
                danhSachMon.getGhiChu()
            });
        }
    }

    public void setTongTien() {
        double thanhTien = 0;
        for (DanhSachMon danhSachMon : listDanhSachMon) {
            thanhTien += danhSachMon.getGia() * danhSachMon.getSoLuong();
        }
        String tongTien = currencyVN.format(thanhTien);
        lblTongTien.setText("Tổng tiền: " + tongTien);
    }

    public void guiBep() {
        int tongMon = listDanhSachMon.size();
        if (IdTable == -1) {
            MsgBox.alert(this, "Chưa chọn bàn cần Order!");
            tabPane.setSelectedIndex(1);
        } else if (tongMon == 0) {
            MsgBox.alert(this, "Bạn chưa chọn món!");
        } else {
            HoaDon hoaDon = new HoaDon();
            hoaDon.setIdTable(IdTable);
            hoaDon.setIdUser(Auth.user.getMaNV());
            hoaDon.setTrangThai("new");
            hoaDon.setNgayTaoHD(XDate.toString(new Date(), "yyyy-MM-dd HH:mm:ss"));
            hoaDonDAO.insert(hoaDon);
            hoaDon = hoaDonDAO.selectBillNew(Auth.user.getMaNV(), IdTable);
            int IdHD = hoaDon.getIdHoaDon();

            try {
                insertListChiTietHD(0, IdHD);
                MsgBox.alert(this, "Đã gửi hóa đơn đến bếp!");
                tabPane.setSelectedIndex(2);
                resetBan();
            } catch (Exception e) {
                MsgBox.error(this, e.getMessage());
            }
        }
    }

    public void capNhat() {
        HoaDon hoaDon = hoaDonDAO.selectBillNew(Auth.user.getMaNV(), IdTable);
        if (hoaDon == null) {
            MsgBox.alert(this, "Vui lòng đợi bếp xác nhận trước khi cập nhật 1 lần nữa!");
        } else {
            hoaDon.setTrangThai("update");
            hoaDonDAO.update(hoaDon);
            int IdHD = hoaDon.getIdHoaDon();

            List<ChiTietHoaDon> list = chiTietHDDAO.selectByHD(hoaDon.getIdHoaDon());
            for (int i = 0; i < list.size(); i++) {
                list.get(i).setGiaSP(listDanhSachMon.get(i).getGia());
                list.get(i).setSoLuong(listDanhSachMon.get(i).getSoLuong());
                list.get(i).setGhiChu(listDanhSachMon.get(i).getGhiChu());
                chiTietHDDAO.update(list.get(i));
            }

            try {
                if (listDanhSachMon.size() > list.size()) {
                    int start = list.size();
                    insertListChiTietHD(start, IdHD);
                }
                MsgBox.alert(this, "Cập nhật thành công!");
                tabPane.setSelectedIndex(2);
                resetBan();
            } catch (Exception e) {
                MsgBox.error(this, e.getMessage());
            }
        }
    }

    public void insertListChiTietHD(int start, int IdHD) throws Exception {
        try {
            for (int i = start; i < listDanhSachMon.size(); i++) {
                ChiTietHoaDon chiTietHoaDon = new ChiTietHoaDon();
                chiTietHoaDon.setIdHoaDon(IdHD);
                chiTietHoaDon.setIdSanPham(listDanhSachMon.get(i).getIdMon());
                chiTietHoaDon.setGiaSP(listDanhSachMon.get(i).getGia());
                chiTietHoaDon.setSoLuong(listDanhSachMon.get(i).getSoLuong());
                chiTietHoaDon.setGhiChu(listDanhSachMon.get(i).getGhiChu());
                chiTietHDDAO.insert(chiTietHoaDon);
            }
        } catch (Exception e) {
            throw new Exception(e.getMessage());
        }
    }

