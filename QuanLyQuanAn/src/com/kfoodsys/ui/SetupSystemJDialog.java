    public void init() {
        setIconImage(XImage.getAppIcon());
        jPanel1.setBackground(new Color(219, 104, 108));
        loadCurrent();
    }

    public void loadCurrent() {
        txtDriver.setText((Auth.cfs.getDriver() == null) ? "com.microsoft.sqlserver.jdbc.SQLServerDriver" : Auth.cfs.getDriver());
        txtDatabase.setText(Auth.cfs.getDatabase());
        txtServer.setText((Auth.cfs.getServer() == null) ? "localhost" : Auth.cfs.getServer());
        txtUser.setText(Auth.cfs.getUsername());
        txtPass.setText(Auth.cfs.getPassword());
    }

    public void setUp() {
        Auth.cfs.setDriver(txtDriver.getText());
        Auth.cfs.setServer(txtServer.getText());
        Auth.cfs.setDatabase(txtDatabase.getText());
        Auth.cfs.setUsername(txtUser.getText());
        Auth.cfs.setPassword(txtPass.getText());
        Object[] obj = {Auth.cfs};
        try {
            new ObjectFile(Auth.fileSys).writeObject(obj);
        } catch (Exception e) {
        }
    }

    public void huyBo() {
        if(MsgBox.confirm(this, "Bạn muốn thoát chương trình?")) {
            System.exit(0);
        }
        
    }


