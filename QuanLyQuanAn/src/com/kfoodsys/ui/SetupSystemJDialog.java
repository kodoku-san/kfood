    private void btnSetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetActionPerformed
        if (txtDriver.getText().trim().length() > 0
                && txtServer.getText().trim().length() > 0
                && txtDatabase.getText().trim().length() > 0
                && txtUser.getText().trim().length() > 0) {
            setUp();
            MsgBox.alert(this, "Lưu thành công!");
            dispose();
        } else {
            MsgBox.error(this, "Vui lòng nhập đủ thông tin các trường!");
        }

    }//GEN-LAST:event_btnSetActionPerformed

